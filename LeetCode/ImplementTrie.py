# A Trie is a Tree in which each node, except the root, contains a character of a string 
# and may have an outgoing edge connecting it to a node that stores the next character of
# the string. Implement a Trie, with the basic operations being insert(word), search(word)
# and startsWith(prefix). You may assume that strings only contain the characters a...z.
class TrieNode:
    def __init__(self):
        self.children = {}
        self.endOfWord = False
class Trie:
    def __init__(self):
        self.root = TrieNode()
    def insert(self, word):
        cur = self.root
        for c in word:
            if c not in cur.children:
                cur.children[c] = TrieNode()
            cur = cur.children[c]
        cur.endOfWord = True
    def search(self, word):
        cur = self.root
        for c in word:
            if c not in cur.children:
                return False
            cur = cur.children[c]
        return cur.endOfWord
    def startsWith(self, prefix):
        cur = self.root
        for c in prefix:
            if c not in cur.children:
                return False
            cur = cur.children[c]
        return True  
